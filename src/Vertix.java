
/**
 * Immutable vertix with X, Y
 * 
 * @author Karl Nicholas
 *
 */
public class Vertix {
	private final int x;
	private final int y;
	public Vertix(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
}
